#este programa mostra os métodos e funções mais usados para trabalhar com listas.

valores = list(range(0,101))

print(valores)
print('')
for valor in valores:
    print(valor)

print('')
print('O maior valor dessa lista é : ', max(valores)) #mostra o maior valor da lista
print('O menor valor dessa lista é : ', min(valores))#mostra o menor valor da lista.
print('A soma de todos esses valores da lista é : ', sum(valores))#mosta a soma de todos os valores.
