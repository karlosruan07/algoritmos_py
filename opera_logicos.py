#Este programa demostra a funcionalidade dos operadores relacionais

# And  nesse operador as duas premissas tem que ser verdade para sair True;
# Or  nesse operador uma duas duas premissas tem que ser verdade para sair True;
# not  esse operador inverte o seu valor EX: True vai ser False  e False vair ser True;

trabalho = False
dinheiro = True

print(trabalho and dinheiro)
print(trabalho or dinheiro)
print(not (trabalho and dinheiro))

