#Este programa mostra as funcionalidades dos operadores aritméticos.

soma = 1 + 1 #soma
subtracao = 2 - 1 #subtração
multiplicacao = 2 * 2 #multiplicação
exponenciação = 2 ** 3 #exponeciação
divisao = 10 / 3 #divisão tradicional
div_frac = 10 // 3 #divisão fracionada
resto_divisao = 10 % 3 #resto da divisão

print('O resultado da soma é : ', soma)
print('O resultado da subtração é : ', subtracao)
print('O resultado da multiplicação é : ', multiplicacao)
print('O resultado da exponenciação é : ', exponenciação)
print('O resultado da divisão é : ', divisao)
print('O resultado da divisão fracionada é : ', div_frac)
print('O resultado da resto do resto da divisão é : ', resto_divisao)


